int gcd(int a, int b)
{
    if (b == 0)
        return a;
    return gcd(b, a % b);
}
int lcm(int a, int b)
{
    return (a / gcd(a, b)) * b;
}
bool comp(int a, int b)
{
    return a > b; // Compare in descending order
}
void solve()
{
    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }
    bool div = false;

    sort(arr.begin(), arr.end());
    for (int i = 1; i < n; i++)
    {
        if (arr[i] % arr[0] != 0)
        {
            div = true;
            break;
        }
    }
    if (n == 0 || n == 1)
    {
        cout << "YES" << endl;
    }
    else if (arr[0] == arr[1])
    {
        if (div)
        {
            cout << "YES" << endl;
        }
        else
        {
            cout << "NO" << endl;
        }
    }
    else
    {
        cout << "YES" << endl;
    }
}
